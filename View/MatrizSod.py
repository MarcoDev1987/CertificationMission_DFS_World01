class MatrizSod:
    def __init__(self, codigo_A = 0, perfil_A = '', codigo_B = 0, perfil_B = '',):
        self.__codigo_A = codigo_A
        self.__perfil_A = perfil_A
        self.__codigo_B = codigo_B
        self.__perfil_B = perfil_B
        

    def setCodigoA(self, codigo_A):
        self.__codigo_A = codigo_A

    def getCodigoA(self):
        return self.__codigo_A
    
    def setPerfilA(self, perfil_A):
        self.__perfil_A = perfil_A

    def getPerfilA(self):
        return self.__perfil_A
    

    def setCodigoB(self, codigo_B):
        self.__codigo_B = codigo_B

    def getCodigoB(self):
        return self.__codigo_B

    def setPerfilB(self, perfil_B):
        self.__perfil_B = perfil_B

    def getPerfilB(self):
        return self.__perfil_B
    
   
    

    